-- Scenario 3
CREATE OR REPLACE TRIGGER CheckTransactionRules
BEFORE INSERT ON Transactions
FOR EACH ROW
DECLARE
    v_bal NUMBER;
BEGIN
    IF :NEW.Amount <= 0 THEN RAISE_APPLICATION_ERROR(-20002, 'Bad amount'); END IF;
    IF :NEW.TransactionType = 'Withdrawal' THEN
        SELECT Balance INTO v_bal FROM Accounts WHERE AccountID = :NEW.AccountID;
        IF v_bal < :NEW.Amount THEN RAISE_APPLICATION_ERROR(-20003, 'No balance'); END IF;
    END IF;
END;
/


