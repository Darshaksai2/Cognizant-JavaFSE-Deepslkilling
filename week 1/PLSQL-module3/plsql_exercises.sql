-- --------------------------------------------------------------------------
-- EXERCISE 2: ERROR HANDLING
-- --------------------------------------------------------------------------
-- Scenario 1
CREATE OR REPLACE PROCEDURE SafeTransferFunds(p_src NUMBER, p_dest NUMBER, p_amt NUMBER) AS
    v_bal NUMBER;
    v_fail EXCEPTION;
BEGIN
    SELECT Balance INTO v_bal FROM Accounts WHERE AccountID = p_src;
    IF v_bal < p_amt THEN RAISE v_fail; END IF;
    UPDATE Accounts SET Balance = Balance - p_amt WHERE AccountID = p_src;
    UPDATE Accounts SET Balance = Balance + p_amt WHERE AccountID = p_dest;
    COMMIT;
EXCEPTION
    WHEN v_fail THEN
        ROLLBACK;
        DBMS_OUTPUT.PUT_LINE('Error: Insufficient funds');
END;
/

-- Scenario 2
CREATE OR REPLACE PROCEDURE UpdateSalary(p_id NUMBER, p_pct NUMBER) AS
BEGIN
    UPDATE Employees SET Salary = Salary + (Salary * (p_pct / 100)) WHERE EmployeeID = p_id;
    IF SQL%NOTFOUND THEN RAISE NO_DATA_FOUND; END IF;
    COMMIT;
EXCEPTION
    WHEN NO_DATA_FOUND THEN
        DBMS_OUTPUT.PUT_LINE('Error: Employee not found');
END;
/

-- Scenario 3
CREATE OR REPLACE PROCEDURE AddNewCustomer(p_id NUMBER, p_name VARCHAR2, p_dob DATE, p_bal NUMBER) AS
BEGIN
    INSERT INTO Customers (CustomerID, Name, DOB, Balance, LastModified) VALUES (p_id, p_name, p_dob, p_bal, SYSDATE);
    COMMIT;
EXCEPTION
    WHEN DUP_VAL_ON_INDEX THEN
        DBMS_OUTPUT.PUT_LINE('Error: Customer already exists');
END;
