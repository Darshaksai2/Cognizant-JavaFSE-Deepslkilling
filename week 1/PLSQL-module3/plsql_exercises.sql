-- Scenario 1
CREATE OR REPLACE FUNCTION CalculateAge(p_dob DATE) RETURN NUMBER AS
BEGIN
    RETURN TRUNC(MONTHS_BETWEEN(SYSDATE, p_dob) / 12);
END;
/

-- Scenario 2
CREATE OR REPLACE FUNCTION CalculateMonthlyInstallment(p_amt NUMBER, p_rate NUMBER, p_yrs NUMBER) RETURN NUMBER AS
    v_m NUMBER := p_yrs * 12;
    v_r NUMBER := (p_rate / 100) / 12;
BEGIN
    IF v_r = 0 THEN RETURN p_amt / v_m; END IF;
    RETURN (p_amt * v_r * POWER(1 + v_r, v_m)) / (POWER(1 + v_r, v_m) - 1);
END;
/

-- Scenario 3
CREATE OR REPLACE FUNCTION HasSufficientBalance(p_id NUMBER, p_amt NUMBER) RETURN BOOLEAN AS
    v_bal NUMBER;
BEGIN
    SELECT Balance INTO v_bal FROM Accounts WHERE AccountID = p_id;
    RETURN v_bal >= p_amt;
EXCEPTION
    WHEN OTHERS THEN RETURN FALSE;
END;
/


